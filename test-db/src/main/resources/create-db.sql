DROP TABLE IF EXIST driver;
CREATE TABLE driver(
    driverId INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    firstName VARCHAR(20) NOT NULL UNIQUE,
    lastName VARCHAR(20) NOT NULL UNIQUE,
    workDate TIMESTAMP NOT NULL,
);
